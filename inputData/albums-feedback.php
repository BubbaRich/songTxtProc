The room in suburban Toronto was dimly lit with bead lights, lava lamps, and candles. Patterned rugs, guitars, amplifiers, and drums were scattered about the floor. The guitarist, bass player, and drummer locked into a fast blues jam, a relaxed acoustic number, a traditional rock anthemAs the music spiraled out, the air filled with the sounds of feedback, backwards guitar, electric twelve-string, and electric sitar. The lava lamps vibrated on the straining amplifiers. Everything was ... very ... beautiful ...
<br>
<br>It was April of 2004, but Geddy, Alex, and I were channeling back to 1966 and 1967, when we were thirteen- and fourteen -year-old beginners. We thought it would be a fitting symbol to commemorate our thirty years together if we returned to our roots and paid tribute to those we had learned from and were inspired by. We thought we might record some of the songs we used to listen to, the ones we painstakingly learned the chords, notes, and drum parts for, and even played in our earliest bands.
<br>
<br>Ironically, I first many of these songs as "cover tunes," played by the local bands around St. Catharines, Ontario, in the mid-sixties. The Who and the Yardbirds were both introduced to me that way. A couple of years later, my own first band, called Momblin' Sumpthin' (from a "L'il Abner" comic, I shall forever have to explain), played Cream's "Crossroads" and the Blue Cheer version of "Summertime Blues."
<br>
<br>At the same time, across the lake in the suburbs of Toronto, Alex played "For What it's Worth" in his first band, The Projection, and later, with Geddy, they also played "Mr. Soul," "Shapes of Things," and "Crossroads," in early versions of Rush, and other bands with names like Dusty Coconuts, Waterlogged Gorilla Fingers, the Wild Woodpecker Revue, and the Aquiline Dimension of the Mind (depending on the day).
<br>
<br>The other tracks on this collection are songs we liked from the era that we thought we could "cover" effectively (meaning not too many backing vocals), and have some fun with. The music celebrates a good time in our lives, and we had a good time celebrating it.
<br>
<br><i>Neil Peart</i>
